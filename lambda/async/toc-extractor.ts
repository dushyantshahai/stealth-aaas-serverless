/**
 * Generate TOC by visually analyzing the PDF with Gemini 3 Flash
 *
 * @param bookId - Book ID for logging
 * @param s3Bucket - S3 bucket containing the PDF
 * @param s3Key - S3 key for the PDF
 * @returns TocExtractionResult with generated TOC
 */
export async function extractToc(
  bookId: string,
  s3Bucket: string,
  s3Key: string
): Promise<TocExtractionResult> {
  logger.info(`[TOC Generator] Starting Gemini vision analysis for book ${bookId}...`);

  try {
    // Step 1: Download PDF from S3
    const getObjectCommand = new GetObjectCommand({
      Bucket: s3Bucket,
      Key: s3Key,
    });

    const response = await s3Client.send(getObjectCommand);
    const pdfBytes = await response.Body?.transformToByteArray();

    if (!pdfBytes) {
      throw new Error('Failed to download PDF from S3');
    }

    const pdfBuffer = Buffer.from(pdfBytes);
    const base64Pdf = pdfBuffer.toString('base64');
    logger.info(`[TOC Generator] PDF loaded (${(pdfBuffer.length / 1024 / 1024).toFixed(1)} MB). Sending to Gemini...`);

    // Step 2: Send entire PDF to Gemini in one shot
    const toc = await analyzeWithGemini(base64Pdf);
    logger.info(`[TOC Generator] Detected ${toc.chapters.length} chapters, ${toc.topics.length} topics`);

    // Step 3: Store in DynamoDB
    const result = await storeTocInDynamoDB(bookId, toc);

    logger.info(`[TOC Generator] TOC extraction complete`, {
      chaptersCount: result.chapters.length,
      topicsCount: result.topics.length,
      subtopicsCount: result.subtopics.length,
      confidence: toc.metadata.overallConfidence,
    });

    return result;
  } catch (error) {
    logger.error('[TOC Generator] Error:', error as Error);
    throw error;
  }
}

