/**
 * Store TOC structure in DynamoDB
 */
async function storeTocInDynamoDB(
  bookId: string,
  toc: AutoGeneratedTOC
): Promise<TocExtractionResult> {
  logger.info('Storing TOC structure in DynamoDB', { bookId });

  const chapters: TocExtractionResult['chapters'] = [];
  const topics: TocExtractionResult['topics'] = [];
  const subtopics: TocExtractionResult['subtopics'] = [];

  // Store chapters
  for (const chapter of toc.chapters) {
    const chapterId = uuidv4();
    chapters.push({
      chapterId,
      title: chapter.title,
      bookId,
      order: chapter.chapterNumber - 1,
      pageStart: chapter.startPage,
      pageEnd: chapter.endPage,
    });

    await docClient.send(new PutCommand({
      TableName: CHAPTERS_TABLE,
      Item: {
        chapterId,
        bookId,
        title: chapter.title,
        order: chapter.chapterNumber - 1,
        pageStart: chapter.startPage,
        pageEnd: chapter.endPage,
        confidence: chapter.confidence,
        createdAt: new Date().toISOString(),
      }
    }));

    // Store in local map for topic lookup
    chapterIdMap.set(chapter.chapterNumber, chapterId);
  }

  // Store topics
  for (const topic of toc.topics) {
    const parentChapter = chapters.find(c => c.order + 1 === topic.parentChapterNumber);
    if (!parentChapter) {
      logger.warn(`Topic ${topic.topicNumber} has no parent chapter ${topic.parentChapterNumber}`);
      continue;
    }

    const topicId = uuidv4();
    topics.push({
      topicId,
      title: topic.title,
      chapterId: parentChapter.chapterId,
      order: parseInt(topic.topicNumber.split('.')[1] || '0', 10),
      pageStart: topic.startPage,
      pageEnd: topic.endPage,
    });

    await docClient.send(new PutCommand({
      TableName: TOPICS_TABLE,
      Item: {
        topicId,
        chapterId: parentChapter.chapterId,
        title: topic.title,
        order: parseInt(topic.topicNumber.split('.')[1] || '0', 10),
        pageStart: topic.startPage,
        pageEnd: topic.endPage,
        confidence: topic.confidence,
        createdAt: new Date().toISOString(),
      }
    }));
  }

  // Store subtopics
  for (const subTopic of toc.subTopics) {
    const parentTopic = topics.find(t => t.order === parseInt(subTopic.parentTopicNumber.split('.')[1] || '0', 10));
    if (!parentTopic) {
      logger.warn(`SubTopic ${subTopic.subTopicNumber} has no parent topic ${subTopic.parentTopicNumber}`);
      continue;
    }

    const subtopicId = uuidv4();
    subtopics.push({
      subtopicId,
      title: subTopic.title,
      topicId: parentTopic.topicId,
      order: parseInt(subTopic.subTopicNumber.split('.').slice(2).join('.') || '0', 10),
      pageStart: subTopic.startPage,
      pageEnd: subTopic.endPage,
    });

    await docClient.send(new PutCommand({
      TableName: SUBTOPICS_TABLE,
      Item: {
        subtopicId,
        topicId: parentTopic.topicId,
        title: subTopic.title,
        order: parseInt(subTopic.subTopicNumber.split('.').slice(2).join('.') || '0', 10),
        pageStart: subTopic.startPage,
        pageEnd: subTopic.endPage,
        confidence: subTopic.confidence,
        createdAt: new Date().toISOString(),
      }
    }));
  }

  // Update book with TOC metadata
  await docClient.send(new PutCommand({
    TableName: BOOKS_TABLE,
    Key: { bookId },
    UpdateExpression: 'SET #tocGenerated = :tocGenerated, #tocConfidence = :confidence, #tocGeneratedAt = :now',
    ExpressionAttributeNames: { '#tocGenerated': 'tocGenerated', '#tocConfidence': 'tocConfidence', '#tocGeneratedAt': 'tocGeneratedAt' },
    ExpressionAttributeValues: {
      ':tocGenerated': true,
      ':confidence': toc.metadata.overallConfidence,
      ':now': new Date().toISOString(),
    },
  }));

  logger.info('TOC structure stored successfully');
  return { chapters, topics, subtopics };
}

